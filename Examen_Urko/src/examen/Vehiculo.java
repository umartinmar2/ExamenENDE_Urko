package examen;

public class Vehiculo {
	String matricula;
	String marca;
	String modelo;
	double precioHora;
	
	
	public Vehiculo(String matricula, String marca, String modelo, double precioHora) {
		super();
		this.matricula = matricula;
		this.marca = marca;
		this.modelo = modelo;
		this.precioHora = precioHora;
	}
	
	

}
